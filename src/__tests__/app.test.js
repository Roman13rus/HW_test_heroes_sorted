import healthLevelSorted from '../app';

test('checking the sorting of data by the heros health level', () => {
  const data = [
    { name: 'мечник', health: 10 },
    { name: 'маг', health: 100 },
    { name: 'лучник', health: 80 },
  ];

  const expected = [
    { name: 'маг', health: 100 },
    { name: 'лучник', health: 80 },
    { name: 'мечник', health: 10 },
  ];
  const received = healthLevelSorted(data);
  expect(received).toEqual(expected);
});
